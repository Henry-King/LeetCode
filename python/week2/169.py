class Solution(object):
    def majorityElement(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        count = 0
        ret = None
        for item in nums:
            if count:
                if item == ret:
                    count += 1
                else:
                    count -= 1
            else:
                ret = item
                count += 1
        return ret


